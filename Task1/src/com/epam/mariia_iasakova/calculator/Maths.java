package com.epam.mariia_iasakova.calculator;

public class Maths {

    double add(double a, double b) {
        return a + b;
    }

    double subtract(double a, double b) {
        return a - b;
    }

    double multiply(double a, double b) {
        return a * b;
    }

    double divide(double a, double b) {
        return a / b;
    }

    double power(double a, double b) {
        return Math.pow(a, b);
    }
}
